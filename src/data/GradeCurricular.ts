interface RawCadeira {
  items: {
    id: string;
    snippet: {
      title: string;
      resourceId: {
        videoId: string;
      };
    };
    contentDetails?: {
      extra?: {
        durationSeconds: number;
      };
    };
  }[];
  extra: {
    name: string;
    durationSeconds: number;
  };
}

const extractRawCadeiraInfo = (
  cadeira: RawCadeira
): { name: string; lessons: Lesson[]; duration?: number } => {
  const lessons: Lesson[] = cadeira.items.map((item) => ({
    id: item.id,
    title: item.snippet.title,
    url: item.snippet.resourceId.videoId,
    duration: item.contentDetails?.extra?.durationSeconds,
    type: "video",
  }));

  return {
    name: cadeira.extra.name,
    lessons: lessons,
    duration: cadeira.extra.durationSeconds,
  };
};

